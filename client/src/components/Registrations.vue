<template>
  <div class="wrap">
    <div class="popup">
      <h2>Registrations</h2>
      <div class="fix">
        <table>
          <tr>
            <th>Name</th>
            <th>Email</th>
            <th>Phone</th>
            <th>Branch</th>
            <th>Year</th>
          </tr>
          <tr v-for="(reg, index) in result" :key="index">
            <th>{{ reg.name }}</th>
            <th>{{ reg.email }}</th>
            <th>{{ reg.phone }}</th>
            <th>{{ reg.branch }}</th>
            <th>{{ reg.year }}</th>
          </tr>
        </table>
      </div>
      <button @click="toggleRegistrations">Close</button>
    </div>
  </div>
</template>

<script>
import fetchData from "@/fetchData.js";

export default {
  name: "Registrations",

  data() {
    return {
      result: [],
    };
  },

  props: {
    toggleRegistrations: Function,
  },

  created() {
    fetchData(
      "registrations",
      {
        email: this.$store.state.email,
        pass: this.$store.state.key,
      },
      (json) => {
        json = JSON.parse(JSON.stringify(json));
        if (json.status) {
          this.result = json.result;
          this.toggleRegistrations();
        } else {
          window.alert("Server Error!");
        }
      }
    );
  },
};
</script>

